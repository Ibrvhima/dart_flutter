// Effectuer des operation arithmetique su deux variables a et b

void main() {
  int a = 5, b = 3;
  print("la somme de $a et $b est: ${a + b}");
  print("la somme de $a et $b est: ${a - b}");
  print("la somme de $a et $b est: ${a * b}");
  print("la somme de $a et $b est: ${a ~/ b}");
  print("la somme de $a et $b est: ${a % b}");
}
